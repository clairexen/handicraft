// implementation of xorshift128
// http://en.wikipedia.org/wiki/Xorshift

var pseudorand_setseed;
var pseudorand;

(function(){

var x = 123456789;
var y = 362436069;
var z = 521288629;
var w = 88675123;

var mask32 = 4294967295;

function func_setseed(seed)
{
	x = 123456789;
	y = 362436069;
	z = 521288629;
	w = seed & mask32;

	for (var i = 0; i < 10; i++)
		func_rand(0);
}

function shl(k, i) {
	// set upper i bits to zero before shifting
	return (k & ~(mask32 << (32-i))) << i;
}

function shr(k, i) {
	// set upper i bits to zero after shifting
	return (k >> i) & ~(mask32 << (32-i));
}

function func_rand(n)
{
	var t = x ^ shl(x, 11);
	x = y; y = z; z = w;
	w ^= shr(w, 19) ^ t ^ shr(t, 8);

	var v = w >= 0 ? w : w + mask32 + 1;
	return n > 0 ? v % n : v;
}

pseudorand_setseed = func_setseed;
pseudorand = func_rand;

})();

